///////////////////////
// 올림, 버림, 반올림 //
//////////////////////
//
Math.ceil(0.1); // 1
Math.floor(0.9); // 0
Math.round(0.5); // 1

//
////////////
// 랜덤값 //
///////////
// 잘안씀
Math.random(); // 0 ~ 1 사이 랜덤값 획득
Math.floor(Math.random() * 10) + 1; // 1 ~ 10

const arr2 = new Uint32Array(10); // 유니크한 랜덤 10개
window.crypto.getRandomValues(arr2);

