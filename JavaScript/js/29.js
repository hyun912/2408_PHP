let arr = [1, 2, 3, 4, 5];
Math.max(...arr); // 5, 안의 값중 최대값 출력

//
////////////
// 최소값 //
///////////
//
Math.min(...arr); // 1, 안의 값중 최소값 출력

//
////////////
// 절대값 //
///////////
// 은근 쓰임
